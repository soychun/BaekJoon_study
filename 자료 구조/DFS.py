# stack, queue, 재귀
# Depth-first search, 깊이 우선 탐색 / 그래프에서 깊은 부분

# 그래프에 대하여 : 노드(node)와 간선(edge)으로 이루어짐. node는 vertice라고도 부름.
# 그래프 탐색이란 하나의 노드를 시작으로 다수의 노드를 방문하는 것.
# 두 노드가 간선으로 연결되어 있다면 두 노드는 인접하다고 표현한다.
#
# 그래프는 인접 행렬과 인접 리스트로 표현할 수 있다.
#
# -인접 행렬 : 2차원 배열로 그래프의 연결 관계를 표현하는 방식
# -인접 리스트 : 리스트로 그래프의 연결 관계를 표현하는 방식

